  $(document).ready(function() {
    $(document).on('click', '.view_data', function() {

      var nome = document.getElementById('nome').value;
      var email = document.getElementById('email').value;
      var assunto = document.getElementById('assunto').value;
      var mensagem = document.getElementById('mensagem').value;

      if ((nome !== '') && (email !== '') && (assunto !== '') && (mensagem !== '')) {
        var dados = {
          nome: nome,
          email: email,
          assunto: assunto,
          mensagem: mensagem
        };
        $.post('fale_conosco.php', dados, function(retorna) {

          if (retorna == 1) {

            document.querySelector('.loading').classList.add("d-block");

            setTimeout(function() {
              document.querySelector('.loading').classList.remove("d-block");
              document.querySelector('.error-message').classList.remove("d-block");
              document.querySelector('.alert-message').classList.remove("d-block");
              document.querySelector('.sent-message').classList.remove("d-block");
              document.querySelector('.error-message').classList.add("d-block");
              // alert(retorna);
            }, 1000);

            setTimeout(function() {
              $(".error-message").fadeOut(2000, function() {
                document.querySelector('.error-message').classList.remove("d-block");
              });
            }, 5000);

            clearTimeout();

          } else {
            document.querySelector('.loading').classList.add("d-block");

            setTimeout(function() {
              document.querySelector('.loading').classList.remove("d-block");
              document.querySelector('.error-message').classList.remove("d-block");
              document.querySelector('.alert-message').classList.remove("d-block");
              document.querySelector('.sent-message').classList.remove("d-block");
              document.querySelector('.sent-message').classList.add("d-block");
              // alert(retorna);
            }, 1000);

            setTimeout(function() {
              $(".sent-message").fadeOut(2000, function() {
                document.querySelector('.sent-message').classList.remove("d-block");
              });
            }, 5000);

            clearTimeout();

            document.getElementById('nome').value = "";
            document.getElementById('email').value = "";
            document.getElementById('assunto').value = "";
            document.getElementById('mensagem').value = "";

          }

        });
      } else {

        document.querySelector('.loading').classList.add("d-block");

        setTimeout(function() {
          document.querySelector('.loading').classList.remove("d-block");
          document.querySelector('.error-message').classList.remove("d-block");
          document.querySelector('.alert-message').classList.remove("d-block");
          document.querySelector('.sent-message').classList.remove("d-block");
          document.querySelector('.alert-message').classList.add("d-block");
        }, 1000);

        setTimeout(function() {
          $(".alert-message").fadeOut(2000, function() {
            document.querySelector('.alert-message').classList.remove("d-block");
          });
        }, 5000);

        var inputnome = document.getElementById('nome').value;
        var inputemail = document.getElementById('email').value;
        var inputassunto = document.getElementById('assunto').value;
        var inputmensagem = document.getElementById('mensagem').value;

        if (inputnome == '') {

          setTimeout(function() {
            document.querySelector('#nome').classList.add("borda-vermelha");
          }, 1000);

        }
        if (inputemail == '') {

          setTimeout(function() {
            document.querySelector('#email').classList.add("borda-vermelha");
          }, 1000);

        }
        if (inputassunto == '') {

          setTimeout(function() {
            document.querySelector('#assunto').classList.add("borda-vermelha");
          }, 1000);

